local toggle_locked <const> = {}
function add_toggle(submenu, label, hints, some, callback)
    return submenu:add_toggle(label, hints, some, function(pid)
        if not toggle_locked[label] then
            toggle_locked[label] = true
            callback(pid)
            toggle_locked[label] = false
        end
    end)
end

