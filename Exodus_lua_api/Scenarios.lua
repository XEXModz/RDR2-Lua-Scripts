local function search_scenarios(search_text)
    local results = {}
    search_text = search_text:lower()
    
    for _, scenario in pairs(ScenarioName) do
        if scenario:lower():find(search_text) then
            table.insert(results, scenario)
        end
    end
    
    return results
end

human_c_search = human_scenarios:add_text_input("Search Scenarios","", 32, true, true,{}, function(search_text)
        if search_text ~= "" then
            local results = search_scenarios(search_text)
            if #results > 0 then
                for _, scenario in pairs(results) do
                    Local_Feature[scenario] = add_toggle(human_scenarios, scenario, {}, function()
                        if Local_Feature[scenario]:get_toggle() then
                            local player_ped = native.player.get_player_ped(0)
                            Hashkey = hash.joaat(scenario)
                            native.task.clear_ped_tasks(player_ped, true, true)
                            native.task.task_start_scenario_in_place_hash(player_ped, Hashkey, 0, true, 0, 0, true, 0)
                            while Local_Feature[scenario]:get_toggle() do
                                Hashkey = hash.joaat(scenario)
                                thread.yield()
                            end
                            native.task.clear_ped_tasks_immediately(player_ped, true, false)
                        end
                    end)
                end
            else
                toast.add_error("No Results Found")
            end
        end
    end)

